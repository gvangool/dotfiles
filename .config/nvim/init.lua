local vimScript = [[
  " Remove trailing whitespaces
  func! DeleteTrailingWS()
      exe "normal mz"
      %s/\s\+$//ge
      exe "normal `z"
  endfunc

  " Django config
  " add a space after/before an opening/closing Django template tag
  func! CleanDjangoTags()
      exe "normal mz"
      %s/{\([{%#]\)\(\|  \+\)\([a-z0-9"']\)/{\1 \3/gei
      %s/\([a-z0-9"']\)\(\|  \+\)\([}%#]\)}/\1 \3}/gei
      exe "normal `z"
  endfunc
]]
vim.cmd(vimScript)

